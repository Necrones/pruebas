public class Descuento {

    float importe;
    float descuento;

    public Descuento(float importe, float descuento) {
        this.importe = importe;
        this.descuento = descuento;
    }
    
    public float getImporte() {
        return importe;
    }

    public float getDescuento() {
        return descuento;
    }
}
